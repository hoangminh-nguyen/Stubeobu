    private boolean checkID(String mssv, JTable table){
        int rowCount = table.getRowCount();
        String rowEntry;
        for (int i = 0; i < rowCount; i++) {
            rowEntry = table.getValueAt(i, 0).toString();
            if (rowEntry.equalsIgnoreCase(mssv)) {
                return true;
            }
        }
        return false;
    }
